
class Product:  
    def __init__(self, 
                 group_name:str, 
                 category_name:str, 
                 product_name:str, 
                 product_price:str,
                 product_amount:str,
                 product_unit:str,
                 product_price_by_unit:str):
        self.group_name = group_name  
        self.category_name = category_name  
        self.product_name = product_name  
        self.product_price = product_price  
        self.product_amount = product_amount  
        self.product_unit = product_unit  
        self.product_price_by_unit = product_price_by_unit  
